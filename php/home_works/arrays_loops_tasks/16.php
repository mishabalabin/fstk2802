<?php
// 16. Дан массив с элементами 1, 2, 3, 4, 5, 6, 7, 8, 9.
//С помощью цикла foreach и оператора if выведите на экран столбец элементов массива, как показано на картинке.
// 1, 2, 3
// 4, 5, 6
// 7, 8, 9

$arr = [1, 2, 3, 4, 5, 6, 7, 8, 9];
$i = 0;

foreach ($arr as $value) {
    echo "$value";
    $i++;

    if($i < 3) {
        echo ", ";
    }

    elseif($i == 3) {
        echo "\n";
        unset($i);
    }
}